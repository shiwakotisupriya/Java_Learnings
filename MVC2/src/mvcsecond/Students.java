package mvcsecond;

public class Students {  //model class
	int id;
	String name;
	String email;
	
	
	public int getId() {
		return id;
	}
	
	public void setId(int sid) {
		this.id= sid;
	}
	

	public String getName() {
		return name;
	}
	
	public void setName(String sname) {
		this.name= sname;
	}
	
	public String getEmail() {
		return email;
	}
	
	public void setEmail(String em) {
		this.email= em;
	}
	
}
