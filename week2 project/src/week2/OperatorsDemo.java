public class OperatorsDemo {
	public static void main(String[]arg) {
		
		int num1 = 12;
		int num2 = 13;
		
		//postfix
		System.out.println("num1 :"+num1);
		System.out.println("num1 :"+num1++);
		System.out.println("num1 :"+num1);
		
		//prefix
		System.out.println("num2 :"+num2);
		System.out.println("num2 :"+ ++num2);
		System.out.println("num2 :"+num2);
		
		
	}
}
