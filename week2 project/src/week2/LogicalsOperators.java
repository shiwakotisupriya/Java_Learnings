package week2;

public class LogicalsOperators {
	public static void main(String[]args) {
		//DECLARE VARIABLES
		int a= 12,b = 5;
		
		//ADDITION OPERATOR
		System.out.println("a+b="+(a+b));
		
		//SUBTRACTION OPERATOR
		System.out.println("a-b="+(a-b));
		
		//MULTIPLICATION OPERATOR
		System.out.println("a*b="+(a*b));
		
		//DIVISIION OPERATOR
		System.out.println("a/b="+(a/b));
		
		//MODULO  OPERATOR
		System.out.println("a%b="+(a%b));
	}
}
