package namiStud;

public class Student {
	private String name;
	private int age;
	private String email;
	
	
	public String getName() {
		return name;
	}
	public void setName(String stname) {
		this.name = stname;
	}
	
	public int getAge() {
		return age;
	}
	
	public void setAge(int ageee) {
		this.age=ageee;
	}
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	

}
